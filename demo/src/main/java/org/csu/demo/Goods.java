package org.csu.demo;

public class Goods {
    private int id;
    private int sellerId;
    private int price;

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public void setId(int id) {

        this.id = id;
    }

    public int getSellerId() {

        return sellerId;
    }

    public void setSellerId(int sellerId) {
        this.sellerId = sellerId;
    }

    public int getId() {

        return id;
    }
}
